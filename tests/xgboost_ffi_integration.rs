/// Locate the Cargo workspace root by walking up from CARGO_MANIFEST_DIR
/// until we find a Cargo.toml containing `[workspace]`.
fn workspace_root() -> PathBuf {
    let mut dir = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    loop {
        let cargo_toml = dir.join("Cargo.toml");
        if cargo_toml.exists() {
            let contents = fs::read_to_string(&cargo_toml).unwrap();
            if contents.contains("[workspace]") {
                return dir;
            }
        }
        assert!(dir.pop(), "Could not find workspace root");
    }
}

fn fixture_path(name: &str) -> PathBuf {
    workspace_root().join("tests").join("fixtures").join(name)
}

/// Assert two f32 values are approximately equal within a tolerance.
fn assert_approx_eq(actual: f32, expected: f32, tol: f32) {
    assert!(
        (actual - expected).abs() < tol,
        "expected {expected} ± {tol}, got {actual} (diff = {})",
        (actual - expected).abs()
    );
}

/// Load the expected predictions fixture (generated by Python script).
fn load_expected_predictions() -> serde_json::Value {
    let path = fixture_path("expected_predictions.json");
    let data = fs::read_to_string(&path).unwrap_or_else(|_| {
        panic!(
            "Missing fixture: {}\nRun: python tests/fixtures/generate_test_fixtures.py",
            path.display()
        )
    });
    serde_json::from_str(&data).unwrap()
}

