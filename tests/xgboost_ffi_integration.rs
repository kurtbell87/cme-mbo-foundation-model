#[test]
fn test_predict_batch_matches_individual_predictions() {
    let model = GbtModel::load(&fixture_path("test_model_2f.json")).unwrap();

    let samples: [[f32; 2]; 3] = [[0.5, 0.5], [-1.0, 1.0], [0.0, 0.0]];
    let flat: Vec<f32> = samples.iter().flat_map(|s| s.iter().copied()).collect();

    let batch = model.predict_batch(&flat, 3, 2).unwrap();
    assert_eq!(batch.len(), 3);

    for (i, sample) in samples.iter().enumerate() {
        let individual = model.predict(sample.as_slice()).unwrap();
        assert_approx_eq(
            batch[i],
            individual,
            1e-7,
        );
    }
}

