// Signal rules:
//   P(directional) < threshold (0.50)              → signal = 0 (HOLD)
//   P(directional) ≥ 0.50 AND P(long) > 0.50      → signal = +1 (LONG)
//   P(directional) ≥ 0.50 AND P(long) ≤ 0.50      → signal = -1 (SHORT)

