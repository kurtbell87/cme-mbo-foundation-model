#[test]
fn test_predict_single_known_inputs_match_python() {
    let model = GbtModel::load(&fixture_path("test_model_2f.json")).unwrap();
    let expected = load_expected_predictions();
    let vectors = expected["2f_model"]["test_vectors"].as_array().unwrap();
    let preds = expected["2f_model"]["predictions"].as_array().unwrap();

    for (i, (vec_json, exp_json)) in vectors.iter().zip(preds.iter()).enumerate() {
        let features: Vec<f32> = vec_json
            .as_array()
            .unwrap()
            .iter()
            .map(|v| v.as_f64().unwrap() as f32)
            .collect();
        let expected_p = exp_json.as_f64().unwrap() as f32;
        let actual_p = model.predict(&features).unwrap();
        assert_approx_eq(actual_p, expected_p, 1e-6);
        // Annotate failures with vector index for easier debugging
        assert!(
            (actual_p - expected_p).abs() < 1e-6,
            "Vector {i}: expected {expected_p}, got {actual_p}"
        );
    }
}

